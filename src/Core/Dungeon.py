from __future__ import annotations

from typing import TYPE_CHECKING

from Core.Map import DMDungeonMap

if TYPE_CHECKING:
    from Core import DMGame
################################################################################

__all__ = ("DMDungeon", )

################################################################################
class DMDungeon:

    __slots__ = (
        "_state",
        "_map",
    )

################################################################################
##### INITIALIZATION ###########################################################
################################################################################
    def __init__(self, game: DMGame):

        self._state: DMGame = game
        self._map: DMDungeonMap = DMDungeonMap(game)

################################################################################
##### PROPERTIES ###############################################################
################################################################################
    @property
    def game(self) -> DMGame:

        return self._state

################################################################################
